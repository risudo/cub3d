void	print_map(char **map)
{
	printf("%s\n", CLEAR);
	for (int i = 0; map[i]; i++)
	{
		for (int j = 0; map[i][j]; j++)
		{
			if (map[i][j] == 'x')
				printf("%s", RED);
			printf("%c", map[i][j]);
			printf("%s", RESET);
		}
		printf("\n");
	}
	printf("\n");
	usleep(10000);
}

